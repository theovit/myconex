"""
MYCONEX RLMAgent — Recursive Language Model Agent
==================================================
Phase 4: Ties together delegation, REPL tools, context management, and
persistent memory into a self-improving, autonomously reasoning agent.

RLMAgent extends HermesMoEAgent and adds:

  1. Complexity-driven task decomposition
     Score task complexity → if above threshold, decompose into sub-tasks and
     delegate them in parallel (agent-as-employee pattern).

  2. Persistent Python REPL
     Agents can execute Python to inspect/transform data without loading
     it into context (avoids context rot).

  3. Hierarchical context management
     RLMContextManager tracks token budgets across the delegation tree.
     Low-priority turns are pruned automatically.

  4. Session + cross-session memory (subconscious layer)
     SessionMemory watches interaction patterns within a session.
     PersistentMemoryStore persists facts, preferences, and summaries
     across sessions.

  5. Self-optimization loop (MiroThinker-inspired)
     After every N tasks, the agent analyses its own performance metrics
     and updates strategy notes stored in persistent memory.

  6. Natural language tool dispatch (VESPER-inspired)
     parse_tool_call() maps plain-English instructions to registered tools.

  7. Discord gateway integration hook
     on_discord_message() / format_discord_response() bridge the agent
     to discord_gateway.py.

  8. MoE sub-agent selection
     Builds on the existing HermesMoE complexity scorer to select the
     appropriate expert for each sub-task delegation.
"""

